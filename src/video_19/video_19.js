// Kiểu dữ liệu Object
// Giải thích: Kiểu dữ liệu Object trong JavaScript là một tập hợp các cặp key-value, trong đó key là tên thuộc tính và value là giá trị của thuộc tính đó. Object có thể chứa nhiều loại dữ liệu khác nhau, bao gồm cả các kiểu dữ liệu nguyên thủy (như Number, String, Boolean) và các Object khác.
const person = {
    name: "Manh Vu",
    age: 20,
    address: "Ha Noi",
    isStudent: true
}

console.log(person);

//Kiểu dữ liệu Array
//Giải thích: Kiểu dữ liệu Array trong JavaScript là một tập hợp các giá trị được lưu trữ theo thứ tự và có thể truy cập thông qua chỉ số (index). Mảng có thể chứa nhiều loại dữ liệu khác nhau, bao gồm cả các kiểu dữ liệu nguyên thủy và các Object.
const numbers = [1, 2, 3, 4, 5];
console.log(numbers);