//Vòng lặp for
for (let i = 0; i <= 5; i++) {
    console.log(i);
}

//Vòng lặp while
let score = 10;
while (score > 5) {
    console.log(`score: ${score}`);
    score--;
}

//Vòng lặp do - while
//Chạy tối thiểu 1 lần cho dù điều kiện trong while đúng hay sai
let score1 = 10;
do {
    console.log(`score: ${score1}`);
    score1--;
} while (score1 > 50);